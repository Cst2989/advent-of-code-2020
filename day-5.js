const ids = [];

function findLocation(direction, collection) {
  let remainingCollection = [...collection];
  [...direction].forEach(c => {
    if (c === 'F' || c === 'L') {
      remainingCollection = [...lowerHalf(remainingCollection)];
    } else {
      remainingCollection = [...upperHalf(remainingCollection)];
    }
  });
  return remainingCollection[0];
}

function lowerHalf(arr) {
  const arrayName = [...arr];
  const half_length = Math.ceil(arrayName.length / 2);
  const leftSide = arrayName.splice(0, half_length);
  return leftSide;
}

function upperHalf(arr) {
  const arrayName = [...arr];
  const half_length = Math.ceil(arrayName.length / 2);
  const rightside = arrayName.splice(half_length, arrayName.length);
  return rightside;
}

